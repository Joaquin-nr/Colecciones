public class mostrarN {
    static void mostrar(int A[]) {
        System.out.println("|******************************************************|");
        for (int i = 0; i < A.length; i++) { System.out.print(" Elemento " + (i + 1) + " -----> " + A[i] + "\n");
        }
        System.out.println("|******************************************************|");
    }
}
