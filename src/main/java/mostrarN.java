public class mostrarN {
    static void mostrar(int ArrayN[]) {
        System.out.println("|******************************************************|");
        for (int i = 0; i < ArrayN.length; i++) { System.out.print(" Elemento " + (i + 1) + " -----> " + ArrayN[i] + "\n");
        }
        System.out.println("|******************************************************|");
    }
}
